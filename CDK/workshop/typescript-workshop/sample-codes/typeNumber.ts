const myAge: number = 30;
const yourAge: number = 20;

console.log(myAge + yourAge); //加算
console.log(myAge - yourAge); //減算
console.log(myAge * yourAge); //乗算
console.log(myAge / yourAge); //除算
console.log(myAge % yourAge); //剰余
console.log(myAge ** yourAge); //べき乗

